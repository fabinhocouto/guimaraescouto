    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(tblProdutosParaSelecionar.getSelectedRow() == -1){
            JOptionPane.showMessageDialog(this, "Selecione o produto.");
            return;
        }
        for (int cod : tblProdutosParaSelecionar.getSelectedRows()) {
             produtosSelecionados.add(produtosParaSelecionar.get(cod));
        }
        //produtosSelecionados.add(produtosParaSelecionar.get(tblProdutosParaSelecionar.getSelectedRow()));
        MyTableModel tableModel = new MyTableModel(Produto.class, produtosSelecionados, tblProdutosSelecionados );
        tblProdutosSelecionados.setModel(tableModel);
        tblProdutosSelecionados.setDefaultRenderer(Object.class, new MyGenericCellRenderer());
        RowSorter<MyTableModel> sorter = new TableRowSorter<MyTableModel>(tableModel);
        tblProdutosSelecionados.setRowSorter(sorter);
        setaTamanhoColunasTabela(tblProdutosSelecionados);
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if(tblProdutosSelecionados.getSelectedRow() == -1){
            JOptionPane.showMessageDialog(this, "Selecione o produto a ser removido da lista.");
            return;
        }
        for (int cod : tblProdutosSelecionados.getSelectedRows()) {
             produtosSelecionados.remove(produtosSelecionados.get(cod));
        }
        //produtosSelecionados.remove(produtosSelecionados.get(tblProdutosSelecionados.getSelectedRow()));
        MyTableModel tableModel = new MyTableModel(Produto.class, produtosSelecionados, tblProdutosSelecionados );
        tblProdutosSelecionados.setModel(tableModel);
        tblProdutosSelecionados.setDefaultRenderer(Object.class, new MyGenericCellRenderer());
        RowSorter<MyTableModel> sorter = new TableRowSorter<MyTableModel>(tableModel);
        tblProdutosSelecionados.setRowSorter(sorter);
        setaTamanhoColunasTabela(tblProdutosSelecionados);
        
    }//GEN-LAST:event_jButton2ActionPerformed

