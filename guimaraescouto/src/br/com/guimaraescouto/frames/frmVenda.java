    private void txtCodBarrasFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtCodBarrasFocusLost
        if(txtCodBarras.getText()!= null
                && !"".equals(txtCodBarras.getText())){ 
            try {
                 if(txtCodBarras.getText().contains("*")){
                     String[] split = txtCodBarras.getText().split("\\*");
                     if(split.length == 1){
                         txtCodBarras.requestFocus();
                         return;
                     }
                     produto = produtoDAO.retornaProdutoPorCodBarras(split[1]);
                     if(produto.getId() == null){
                         int result = JOptionPane.showConfirmDialog(this, "Produto não encontrado. Deseja adicionar?","Produto não encontrado",JOptionPane.OK_OPTION,JOptionPane.QUESTION_MESSAGE);
                         if(result == 0){
                             frmAdicionarProduto dialog = new frmAdicionarProduto(new javax.swing.JFrame(), true, produtoDAO,null,this);
                             dialog.setVisible(true);
                             txtCodBarras.requestFocus();
                             flagFinalizar = false;
                             return;
                         }else{
                             txtCodBarras.setText(null);
                             txtCodBarras.requestFocus();
                             flagFinalizar = false;
                             return;
                         }
                     }else{
                         if(produto.getSecao()){
                            txtValorUnitario.requestFocus();
                            flagFinalizar = false;
                            quantidade = new Integer(split[0]);
                            return;
                         }else{
                            quantidade = new Integer(split[0]);
                            insereItemVenda(produto);
                            txtCodBarras.setText(null);
                            txtCodBarras.requestFocus();
                            flagFinalizar = false;
                            return;
                        }
                     }
                 }else if(txtCodBarras.getText().toUpperCase().contains("P")){
                     txtCodBarras.setText(txtCodBarras.getText().toUpperCase().replace("P", ""));
                     produto = produtoDAO.retornaProdutoPorCodBarras(txtCodBarras.getText());
                     if(produto.getId() == null){
                         int result = JOptionPane.showConfirmDialog(this, "Produto não encontrado. Deseja adicionar?","Produto não encontrado",JOptionPane.OK_OPTION,JOptionPane.QUESTION_MESSAGE);
                         if(result == 0){
                             frmAdicionarProduto dialog = new frmAdicionarProduto(new javax.swing.JFrame(), true, produtoDAO,null,this );
                             dialog.setVisible(true);
                         }else{
                             txtCodBarras.setText(null);
                             txtCodBarras.requestFocus();
                             flagFinalizar = false;
                             return;
                         }
                     }
                     quantidade = new Integer(1);
                     txtValorUnitario.requestFocus();
                     flagFinalizar = false;
                     return;
                 }
                 // TODO add your handling code here:
                 produto = produtoDAO.retornaProdutoPorCodBarras(txtCodBarras.getText());
                 if(produto.getId() == null){
                    int result = JOptionPane.showConfirmDialog(this, "Produto não encontrado. Deseja adicionar?","Produto não encontrado",JOptionPane.OK_OPTION,JOptionPane.QUESTION_MESSAGE);
                    if(result == 0){
                        frmAdicionarProduto dialog = new frmAdicionarProduto(new javax.swing.JFrame(), true, produtoDAO,null,this );
                        dialog.setVisible(true);
                    }else{
                        txtCodBarras.setText(null);
                        txtCodBarras.requestFocus();
                        flagFinalizar = false;
                        return;
                    }
                 }
                 produto = produtoDAO.retornaProdutoPorCodBarras(txtCodBarras.getText());
                 quantidade = new Integer(1);
                 if(produto.getSecao()){
                     txtValorUnitario.requestFocus();
                     flagFinalizar = false;
                     return;
                 }else{
                     insereItemVenda(produto);
                     txtCodBarras.setText(null);
                     txtCodBarras.requestFocus();
                     flagFinalizar = false;
                     return;
                 }

             } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Erro ao tentar adicionar o item. Tente novamente.");
                txtCodBarras.setText(null);
                txtCodBarras.requestFocus();
                flagFinalizar = false;
                return;
             }
        }else{
            if(!flagFinalizar){
                txtCodBarras.requestFocus();
            }
        }
    }//GEN-LAST:event_txtCodBarrasFocusLost

