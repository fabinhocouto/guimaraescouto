    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if ("".equals(txtNome.getText().trim())) {
            JOptionPane.showMessageDialog(this, "Favor adicionar o nome");
            txtNome.requestFocus();
            return;
        }
        
        if ("".equals(txtUsuario.getText().trim())) {
            JOptionPane.showMessageDialog(this, "Favor adicionar o usu치rio");
            txtUsuario.requestFocus();
            return;
        }
        
        if ("".equals(txtSenha.getText().trim())) {
            JOptionPane.showMessageDialog(this, "Favor adicionar a senha");
            txtSenha.requestFocus();
            return;
        }
        
        if(!txtSenha.getText().equals(txtSenhaConfirmacao.getText())){
            JOptionPane.showMessageDialog(this, "Senha diferente da Senha de Confirma칞칚o","Aten칞칚o",JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        int result = JOptionPane.showConfirmDialog(this, "Deseja alterar o usu치rio?","Alterar Usu치rio",JOptionPane.OK_CANCEL_OPTION,JOptionPane.QUESTION_MESSAGE);
        if(result == 2){
            return;
        }
        
        usuarioEmAlteracao.setNome(txtNome.getText());
        usuarioEmAlteracao.setUsuario(txtUsuario.getText());
        usuarioEmAlteracao.setSenha(txtSenha.getText());
        try {
            usuarioDAO.atualizarUsuario(usuarioEmAlteracao);
        } catch (SQLException ex) {
            System.err.println("Erro" + ex);
            JOptionPane.showMessageDialog(this, "Erro ao tentar atualizar usu치rio.","Erro",JOptionPane.ERROR_MESSAGE);
            return;
        }
        control.loadInitialData();
        setVisible(false);
    }//GEN-LAST:event_jButton1ActionPerformed

